// Tipos de funcao

// Declarativas

function funcao (){
    console.log('Sou uma mensagem declarativa');
}

funcao ();

// Expressões de função

var funcao1 = function funcao2(){
    console.log('sou uma mensagem expressiva');
}

funcao1 ();

var funcao3 = function(){
    console.log('sou uma mensagem expressiva');
}

funcao3 ();

// Arrow Function

var funcaoArrow = () => {
    console.log('Sou uma arrow function');
}

funcaoArrow ();